
n1 = int(input("Ingrese numero 1: "))
n2 = int(input("Ingrese numero 2: "))

if n1==n2:
    print("Los numeros ingresados son iguales")
elif n1!=n2:
    print("Los numeros ingresados son diferentes")

if n1<n2:
    print("El numero 1 es menor que el numero 2")
elif n1>n2:
    print("El numero 1 es mayor que el numero 2")

if n1<=n2:
    print("El numero 1 es menor o igual a numero 2")
elif n1>=n2:
    print("El numero 1 es mayor o igual a numero 2")

